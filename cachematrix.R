## Caching the Inverse of a Matrix

## makeCacheMatrix sets the matrix to be used in the cachesolve function below.

makeCacheMatrix <- function(x = matrix()) {
        
        inv <- NULL
        
        set <- function(y) {
                x <<- y
                inv <<- NULL
        }
        
        get <- function(){
                x
        }
        
        setinv <- function(inverse){
                inv <<- inverse
        } 
        
        getinv <- function(){
                inv
        }
        
        list(set = set, get = get, setinv = setinv, getinv = getinv)
}


## CachSolve function produces the inverse of the Matrix from the above function

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        
        inv <- x$getinv()
        
        if(!is.null(inv)) {
                message("getting cached inverse data")
                return(inv)
        }
        
        data <- x$get()
        inv <- solve(data, ...)
        x$setinv(inv)
        return(inv)
}
